void main() {
  /*
  Write a program that calculates
the average of five given integers.*/

  // Five given integers
  int num1 = 10;
  int num2 = 20;
  int num3 = 30;
  int num4 = 40;
  int num5 = 50;

  // Calculate total
  int total = num1 + num2 + num3 + num4 + num5;

  // Calculate average
  double average = total / 5;

  // Print result
  print('The average of the five numbers is: $average');
}
